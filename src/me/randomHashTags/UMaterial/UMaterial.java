            org.bukkit.potion.PotionData pd = null;
            if(a) {
                pm = (org.bukkit.inventory.meta.PotionMeta) is.getItemMeta();
                pd = new org.bukkit.potion.PotionData(type, type.isExtendable() ? extended : false, type.isUpgradeable() ? upgraded : false);
            }
